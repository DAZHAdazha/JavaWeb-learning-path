package cn.itcast.junit;
/**
    Calculator class
 */
public class Calculator {

    /**
     * addition
     * @param a
     * @param b
     * @return
     */
    public int add (int a, int b){
        return a + b;
    }

    /**
     * subtraction
     * @param a
     * @param b
     * @return
     */
    public int sub (int a, int b){
        return a - b;
    }

}





